#include "cu_mapping.h"

void	mgnl_ft_strcpy(char *src, char *cpy, int len)
{
	int	i;

	i = 0;
	while (i < len)
	{
		cpy[i] = src[i];
		i ++;
	}
	cpy[i] = '\0';
}
