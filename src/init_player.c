#include "cub3d.h"

void	init_player(t_player *p)
{
	p->pos_x = 2;
	p->pos_y = 2;
	p->dir_x = 0;
	p->dir_y = 1;
	p->cam_x = 0.66;
	p->cam_y = 0;
	p->move_speed = 0.1;
	p->rot_speed = 0.1;
}

